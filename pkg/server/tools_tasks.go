// handleSearchResponses searches response text
func (s *Server) handleSearchResponses(ctx context.Context, req *mcp.CallToolRequest, args searchResponsesArgs) (*mcp.CallToolResult, any, error) {
	limit := args.Limit
	if limit == 0 {
		limit = 50
	}

	searchReq := &types.ResponseSearchRequest{
		Query: args.SearchTerm,
	}

	responses, err := s.mythicClient.SearchResponses(ctx, searchReq)
	if err != nil {
		return nil, nil, translateError(err)
	}

	// Apply limit manually since SDK doesn't support it in SearchResponses
	if len(responses) > limit {
		responses = responses[:limit]
	}

	data, err := json.MarshalIndent(responses, "", "  ")
	if err != nil {
		return nil, nil, err
	}

	return &mcp.CallToolResult{
		Content: []mcp.Content{
			&mcp.TextContent{
				Text: fmt.Sprintf("Search results for '%s' (%d total, limit %d):\n\n%s",
					args.SearchTerm, len(responses), limit, string(data)),
			},
		},
	}, responses, nil
}

