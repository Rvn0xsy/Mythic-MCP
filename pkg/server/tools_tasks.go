// handleWaitForTask waits for a task to complete
func (s *Server) handleWaitForTask(ctx context.Context, req *mcp.CallToolRequest, args waitForTaskArgs) (*mcp.CallToolResult, any, error) {
	timeout := args.Timeout
	if timeout == 0 {
		timeout = 60
	}

	err := s.mythicClient.WaitForTaskComplete(ctx, args.TaskID, timeout)
	if err != nil {
		return nil, nil, translateError(err)
	}

	// Get the task details after completion
	task, err := s.mythicClient.GetTask(ctx, args.TaskID)
	if err != nil {
		return nil, nil, translateError(err)
	}

	data, err := json.MarshalIndent(task, "", "  ")
	if err != nil {
		return nil, nil, err
	}

	return &mcp.CallToolResult{
		Content: []mcp.Content{
			&mcp.TextContent{
				Text: fmt.Sprintf("Task %d completed with status: %s\n\n%s",
					task.DisplayID, task.Status, string(data)),
			},
		},
	}, task, nil
}

