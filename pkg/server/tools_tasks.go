// registerTasksTools registers task and response management MCP tools
func (s *Server) registerTasksTools() {
	// Task operations

	// mythic_issue_task - Issue a task to a callback
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_issue_task",
		Description: "Issue a task/command to a callback (agent)",
	}, s.handleIssueTask)

	// mythic_get_task - Get task details
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_task",
		Description: "Get details of a specific task by ID",
	}, s.handleGetTask)

	// mythic_update_task - Update task properties
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_update_task",
		Description: "Update a task's properties (comment, completed, etc.)",
	}, s.handleUpdateTask)

	// mythic_get_callback_tasks - Get tasks for a callback
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_callback_tasks",
		Description: "Get all tasks for a specific callback with optional limit",
	}, s.handleGetCallbackTasks)

	// mythic_get_tasks_by_status - Filter tasks by status
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_tasks_by_status",
		Description: "Get tasks for a callback filtered by status (submitted, processing, completed, error, etc.)",
	}, s.handleGetTasksByStatus)

	// mythic_wait_for_task - Wait for task completion
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_wait_for_task",
		Description: "Wait for a task to complete with timeout",
	}, s.handleWaitForTask)

	// mythic_get_task_output - Get task output/responses
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_task_output",
		Description: "Get the output/responses for a task",
	}, s.handleGetTaskOutput)

	// mythic_reissue_task - Reissue a task
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_reissue_task",
		Description: "Reissue a task with the same parameters",
	}, s.handleReissueTask)

	// mythic_reissue_task_with_handler - Reissue task with handler
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_reissue_task_with_handler",
		Description: "Reissue a task with handler for callback structure updates",
	}, s.handleReissueTaskWithHandler)

	// mythic_get_task_artifacts - Get task artifacts
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_task_artifacts",
		Description: "Get artifacts (IOCs) generated by a task",
	}, s.handleGetTaskArtifacts)

	// mythic_request_opsec_bypass - Request OPSEC bypass
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_request_opsec_bypass",
		Description: "Request bypass for OPSEC checks on a blocked task",
	}, s.handleRequestOpsecBypass)

	// mythic_add_mitre_attack_to_task - Add MITRE ATT&CK mapping
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_add_mitre_attack_to_task",
		Description: "Add MITRE ATT&CK technique mapping to a task",
	}, s.handleAddMitreAttackToTask)

	// Response operations

	// mythic_get_task_responses - Get responses for a task
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_task_responses",
		Description: "Get all responses for a specific task",
	}, s.handleGetTaskResponses)

	// mythic_get_callback_responses - Get responses for a callback
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_callback_responses",
		Description: "Get responses for a callback with optional limit",
	}, s.handleGetCallbackResponses)

	// mythic_get_response - Get specific response
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_response",
		Description: "Get details of a specific response by ID",
	}, s.handleGetResponse)

	// mythic_get_latest_responses - Get latest responses
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_latest_responses",
		Description: "Get the latest responses for an operation",
	}, s.handleGetLatestResponses)

	// mythic_search_responses - Search response text
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_search_responses",
		Description: "Search for responses containing specific text",
	}, s.handleSearchResponses)

	// mythic_get_response_statistics - Get response statistics
	mcp.AddTool(s.mcpServer, &mcp.Tool{
		Name:        "mythic_get_response_statistics",
		Description: "Get statistics about responses for a specific task",
	}, s.handleGetResponseStatistics)
}

