// Tool handlers

// handleGetC2Profiles retrieves all C2 profiles
func (s *Server) handleGetC2Profiles(ctx context.Context, req *mcp.CallToolRequest, args getC2ProfilesArgs) (*mcp.CallToolResult, any, error) {
	profiles, err := s.mythicClient.GetC2Profiles(ctx)
	if err != nil {
		return nil, nil, translateError(err)
	}

	data, err := json.MarshalIndent(profiles, "", "  ")
	if err != nil {
		return nil, nil, err
	}

	return &mcp.CallToolResult{
		Content: []mcp.Content{
			&mcp.TextContent{
				Text: fmt.Sprintf("All C2 profiles (%d total):\n\n%s", len(profiles), string(data)),
			},
		},
	}, wrapList(profiles), nil
}

// handleGetC2Profile retrieves a specific C2 profile by ID, including its
// current configuration parameter values.
func (s *Server) handleGetC2Profile(ctx context.Context, req *mcp.CallToolRequest, args getC2ProfileArgs) (*mcp.CallToolResult, any, error) {
	profile, err := s.mythicClient.GetC2ProfileByID(ctx, args.ProfileID)
	if err != nil {
		return nil, nil, translateError(err)
	}

	// Fetch configuration parameters and populate the profile's Parameters map
	params, paramErr := s.mythicClient.GetC2ProfileParameters(ctx, args.ProfileID)
	if paramErr == nil && len(params) > 0 {
		configuration := make(map[string]interface{}, len(params))
		for _, p := range params {
			configuration[p.Name] = map[string]interface{}{
				"value":          p.DefaultValue,
				"type":           p.ParameterType,
				"required":       p.Required,
				"description":    p.Description,
				"verifier_regex": p.VerifierRegex,
			}
			// Include choices for selection types
			if p.Choices != "" && p.Choices != "[]" {
				configuration[p.Name].(map[string]interface{})["choices"] = p.Choices
			}
		}
		profile.Parameters = configuration
	}

	data, err := json.MarshalIndent(profile, "", "  ")
	if err != nil {
		return nil, nil, err
	}

	status := "Stopped"
	if profile.Running {
		status = "Running"
	}

	return &mcp.CallToolResult{
		Content: []mcp.Content{
			&mcp.TextContent{
				Text: fmt.Sprintf("C2 Profile %d (%s): %s\nType: %s\n\n%s",
					profile.ID, status, profile.Name, profile.Description, string(data)),
			},
		},
	}, profile, nil
}

// handleCreateC2Instance creates a new C2 profile instance
func (s *Server) handleCreateC2Instance(ctx context.Context, req *mcp.CallToolRequest, args createC2InstanceArgs) (*mcp.CallToolResult, any, error) {
	createReq := &types.CreateC2InstanceRequest{
		C2ProfileID:  args.C2ProfileID,
		InstanceName: args.InstanceName,
		C2Instance:   args.C2Instance,
	}

	err := s.mythicClient.CreateC2Instance(ctx, createReq)
	if err != nil {
		return nil, nil, translateError(err)
	}

	return &mcp.CallToolResult{
		Content: []mcp.Content{
			&mcp.TextContent{
				Text: fmt.Sprintf("Successfully created C2 instance '%s' for profile ID %d",
					args.InstanceName, args.C2ProfileID),
			},
		},
	}, map[string]interface{}{
		"instance_name": args.InstanceName,
		"c2profile_id":  args.C2ProfileID,
		"success":       true,
	}, nil
}

